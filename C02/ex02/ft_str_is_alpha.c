int		ft_str_is_alpha(char *str)
{
	char *i;

	i = str;
	while (*i != '\0')
	{
		if (*i < 'A')
			return (0);
		if ((*i > 'Z' && *i < 'a') || (*i > 'z'))
			return (0);
		i++;
	}
	return (1);
}
