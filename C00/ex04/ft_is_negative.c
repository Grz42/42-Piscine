#include <unistd.h>

void	ft_is_negative(int n)
{
	char pos;
	char neg;

	if (n >= 0)
	{
		pos = 'P';
		write(1, &pos, 1);
	}
	else
	{
		neg = 'N';
		write(1, &neg, 1);
	}
}
